/**
 * Convert voice speed setting to speech rate value
 * Rate values: 0.0 to 1.0 (iOS) or 0.0 to 2.0 (Android)
 * - slow: 0.3 (slower)
 * - default: 0.4 (normal)
 * - fast: 0.6 (faster)
 */
export function getVoiceSpeedRate(speed: VoiceSpeed): number {
  switch (speed) {
    case 'slow':
      return 0.3;
    case 'default':
      return 0.6;
    case 'fast':
      return 1.0;
    default:
      return 0.6;
  }
}

